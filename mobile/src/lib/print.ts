function htmlDoc(title: string, body: string): string {
  return `<!doctype html>
<html>
<head>
<meta charset="utf-8" />
<meta name="viewport" content="width=device-width, initial-scale=1" />
<title>${escapeHtml(title)}</title>
<style>${DOC_STYLE}</style>
</head>
<body>${body}</body>
</html>`;
}

/**
 * Build a printable A4 question paper for a single copy. The signed QR is
 * embedded inline (the SVG is generated by the caller via qrSvg()).
 */
export function buildPaperHtml(result: PrintResult, copy: PrintedCopy, qrSvgString: string): string {
  return htmlDoc(
    `${result.paper.title} — Copy #${copy.copy_number}`,
    copySection(result, copy, qrSvgString)
  );
}

/**
 * Build ONE document containing every copy, each on its own page(s) with its
 * own signed QR. `copies` pairs each printed copy with its pre-rendered QR SVG.
 */
export function buildAllCopiesHtml(
  result: PrintResult,
  copies: { copy: PrintedCopy; qrSvg: string }[]
): string {
  const body = copies.map(({ copy, qrSvg }) => copySection(result, copy, qrSvg)).join('');
  return htmlDoc(`${result.paper.title} — ${copies.length} copies`, body);
}

/** Print an HTML document. Web uses a hidden iframe; native uses expo-print. */
export async function printHtml(html: string): Promise<void> {
  if (Platform.OS === 'web') {
    const iframe = document.createElement('iframe');
    iframe.style.cssText = 'position:fixed;right:0;bottom:0;width:0;height:0;border:0;';
    document.body.appendChild(iframe);
    const doc = iframe.contentWindow?.document;
    if (!doc) return;
    doc.open();
    doc.write(html);
    doc.close();
    const win = iframe.contentWindow;
    const cleanup = () => setTimeout(() => iframe.remove(), 1000);
    win?.addEventListener('afterprint', cleanup);
    setTimeout(() => {
      win?.focus();
      win?.print();
    }, 300);
    return;
  }
  await Print.printAsync({ html });
}

/** Native-only: render to a PDF file and open the share sheet. */
export async function sharePaperPdf(html: string): Promise<void> {
  if (Platform.OS === 'web') {
    await printHtml(html);
    return;
  }
  const { uri } = await Print.printToFileAsync({ html });
  if (await Sharing.isAvailableAsync()) {
    await Sharing.shareAsync(uri, { mimeType: 'application/pdf' });
  }
}
