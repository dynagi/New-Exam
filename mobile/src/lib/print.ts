import { PaperQuestionContent, PrintedCopy, PrintResult } from './types';

function escapeHtml(s: string): string {
  return String(s)
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;');
}

function questionHtml(q: PaperQuestionContent, index: number): string {
  const marks = `<span class="marks">[${q.marks} mark${q.marks === 1 ? '' : 's'}]</span>`;
  const meta =
    q.question_type === 'theoretical'
      ? '<span class="qtype">Theory</span>'
      : '<span class="qtype">MCQ</span>';
  let opts = '';
  if (q.question_type === 'mcq' && q.options.length) {
    opts =
      '<ol class="options" type="A">' +
      q.options.map((o) => `<li>${escapeHtml(o)}</li>`).join('') +
      '</ol>';
  } else {
    opts = '<div class="answer-space"></div>';
  }
  return `
    <li class="question">
      <div class="q-head">
        <span class="q-body"><b>Q${index + 1}.</b> ${escapeHtml(q.body)}</span>
        <span class="q-tags">${meta} ${marks}</span>
      </div>
      ${opts}
    </li>`;
}

/** One copy rendered as a self-contained, page-breaking section. */
function copySection(result: PrintResult, copy: PrintedCopy, qrSvgString: string): string {
  const p = result.paper;
  const header = `
    <div class="header">
      <div class="header-text">
        <div class="exam">${escapeHtml(p.exam_name || 'Examination')}</div>
        <h1>${escapeHtml(p.title)}</h1>
        <div class="sub">
          Subject: <b>${escapeHtml(p.subject)}</b> &nbsp;·&nbsp;
          Questions: <b>${p.questions.length}</b> &nbsp;·&nbsp;
          Total marks: <b>${p.total_marks}</b>
        </div>
        <div class="copyline">Copy #${copy.copy_number} of ${result.copies} &nbsp;·&nbsp; ${escapeHtml(
          result.location
        )}</div>
      </div>
      <div class="qr">
        ${qrSvgString}
        <div class="qr-cap">Scan to verify · signed</div>
      </div>
    </div>`;

  const instructions = `
    <div class="instructions">
      <b>Instructions:</b> Do not open this booklet until instructed. This copy is uniquely
      watermarked and QR-signed; its chain of custody is tracked. Any leak is traceable to this copy.
    </div>`;

  const questions =
    '<ol class="questions">' + p.questions.map((q, i) => questionHtml(q, i)).join('') + '</ol>';

  return `<section class="copy">${header}${instructions}${questions}</section>`;
}

